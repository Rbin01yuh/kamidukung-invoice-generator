// zh
const zhNums = ["零", "一", "二", "三", "四", "五", "六", "七", "八", "九"];
function zhWords(n: number): string {
  n = Math.floor(n);
  if (n === 0) return "零";
  if (n >= 100000000) {
    const high = Math.floor(n / 100000000);
    const low = n % 100000000;
    return zhWords(high) + "亿" + (low > 0 ? (low < 10000000 ? "零" : "") + zhWords(low) : "");
  }
  if (n >= 10000) {
    const high = Math.floor(n / 10000);
    const low = n % 10000;
    return zhWords(high) + "万" + (low > 0 ? (low < 1000 ? "零" : "") + zhWords(low) : "");
  }
  let result = "";
  let isZero = false;
  
  const thousand = Math.floor(n / 1000);
  if (thousand > 0) {
    result += zhNums[thousand] + "千";
  } else {
    isZero = true;
  }
  
  const remain1 = n % 1000;
  const hundred = Math.floor(remain1 / 100);
  if (hundred > 0) {
    if (isZero && result !== "") result += "零";
    result += zhNums[hundred] + "百";
    isZero = false;
  } else if (result !== "") {
    isZero = true;
  }
  
  const remain2 = remain1 % 100;
  const ten = Math.floor(remain2 / 10);
  if (ten > 0) {
    if (isZero && result !== "") result += "零";
    if (ten === 1 && result === "") {
      result += "十";
    } else {
      result += zhNums[ten] + "十";
    }
    isZero = false;
  } else if (result !== "") {
    isZero = true;
  }
  
  const ones = remain2 % 10;
  if (ones > 0) {
    if (isZero && result !== "") result += "零";
    result += zhNums[ones];
  }
  return result;
}

