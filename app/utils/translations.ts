export function numberToWords(n: number, lang: LanguageCode): string {
  const num = Math.floor(n);
  if (num === 0) {
    switch (lang) {
      case "id":
      case "ms": return "Nol Rupiah";
      case "zh": return "零卢比";
      case "ja": return "零ルピア";
      case "ko": return "영 루피아";
      default: return "Zero Rupiah";
    }
  }

  switch (lang) {
    case "id":
    case "ms":
      return idWords(num) + " Rupiah";
    case "zh":
      return zhWords(num) + "卢比";
    case "ja":
      return jaWords(num) + "ルピア";
    case "ko":
      return koWords(num) + " 루피아";
    default:
      return enWords(num) + " Rupiah";
  }
}
