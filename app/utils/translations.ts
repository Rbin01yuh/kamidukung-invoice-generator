// central state management helper for LocalStorage
export function loadLanguage(): LanguageCode {
  if (typeof window !== "undefined") {
    const saved = localStorage.getItem("app_lang") as LanguageCode;
    const allLangs = ["id", "en", "zh", "ja", "ko", "es", "fr", "de", "it", "pt", "ru", "ar", "hi", "tr", "vi", "th", "nl", "pl", "sv", "tl", "ms"];
    if (saved && allLangs.includes(saved)) {
      return saved;
    }
  }
  return "id"; // Default to Indonesian
}

export function saveLanguage(lang: LanguageCode) {
  if (typeof window !== "undefined") {
    localStorage.setItem("app_lang", lang);
  }
}

